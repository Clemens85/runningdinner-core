package org.runningdinner.core;

public class NoPossibleRunningDinnerException extends Exception {

	private static final long serialVersionUID = -8611468413016877441L;

	public NoPossibleRunningDinnerException() {
		super();
	}

	public NoPossibleRunningDinnerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoPossibleRunningDinnerException(String arg0) {
		super(arg0);
	}

	public NoPossibleRunningDinnerException(Throwable arg0) {
		super(arg0);
	}

}
